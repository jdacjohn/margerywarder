                  <p class="p2">
                  	What can separate you from God's love? Can wealth? Poverty? Queen Victoria's despised Canning Town? Can
                    life? Death? Shattered dreams? The bottomless Atlantic or a failing farm in a strange land? War? Malicious
                    gossip? The Kisbey's 19th century lives provided frequent tests of their faith and character. The author's
                    "Novel Approach to Bible Study" offers opportunities for readers to dig into God's Word to stretch and 
                    cherish their lives, too.<br />
      							Unto All Generations follows Emanuel and Esther from England to Michigan in time for the "un-Civil" War. If
                    Emanuel enlists with Michigan's Calvary to meet farm debts and take a mouth from the table, can Londoner
                    Esther tend family and farm while waiting for her beloved's return? His letters home are woven into "Unto
                    All Generations." Their lives unfold with quaintness, humor, history, and a good dose of storytelling,
                    creating a tale perhaps not unlike many who ventured throught he nineteenth century, or even similar to 
                    circumstances families face today.<br />
      							Unto All Generations is the revision of Leaves That Did Not Wither (2009), but now separates each chapter
                    with an optional in-depth Bible study entitled "Holding onto Faith and Humor When God Dismantles Our
                    Comfort Zones." Those dealing with imperfect lives may find it appropriate for individual or group study.<br />
      							Margery Kisby Warder grew up near the farm where Esther spent her last years. Margery creatively spent her
                    adult life teaching Bible and assisting in her husband's ministry, raising their children, and occasionally
                    finding other ways to feed her passion for storytelling. When her children were almost grown, she held Esther's
                    letters in her hand and knew this novel needed to be written. To see her other writing visit
