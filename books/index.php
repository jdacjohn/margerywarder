                    just devour "Elizabeth's Prodigal."<br />
      							In 1875, Margery Warder's grandfather ran off at age 13, probably taking little more than an overly 
                    confident estimate of his wisdom and a hefty chip on his shoulder. Little is known of his adventures 
                    until he shows up on a census a few states away in 1880. Margery's first novel relied upon family 
                    history and official documents to tell of her Great-grandparents' lives, but in "Elizabeth's Prodigal," 
                    it's the next generation's turn to tell a story, and fiction rules.<br />
      							The headstrong runaway in "Elizabeth's Prodigal" is not Alfred, the author's grandfather. Fictitious 
                    Albert, however, plans to cast off just about everything that matters to those who care about him. His 
                    antics take him into adventure, conflict, grandiose plans for success, trickery, opportunities to gain 
                    wisdom, and before the book closes, even a bit of romance. Will Elizabeth's prodigal pick up truths 
