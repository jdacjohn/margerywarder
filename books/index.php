                  into a carriage headed for London. They step out, grateful to go their separate ways. Still, Aunt Emma notices 
                  something about Emanuel that might be worth remembering. Foes threaten life regardless of where it's lived, but 
                  foes waiting for Esther and Emanuel in Canning Town seem to have bigger clubs. Dare the couple cross the Atlantic 
                  to join families in America? 1858 becomes the eve of America's Civil War. If the "un-civil" war continues, will it 
                  entangle Esther and Emanuel? Leaves That Did Not Wither is based upon research into the times and lives of Esther 
                  Copsey and Emanuel Kisbey. Esther kept Civil War letters for her descendents. Now one passes the letters on to you. 
                  Lives of unnoticed English Americans unfold with quaintness, humor, history and a good dose of storytelling into a 
                  tale perhaps not unlike circumstances of many who ventured into the nineteenth century.</p>

