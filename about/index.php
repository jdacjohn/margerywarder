                <p class="p1-about">Margery Kisby Warder loved "writing" as soon as she could hold a pencil or a crayon. 
                Her first writing was on the wallpaper in her parents' living room, so as reviewers everywhere would agree, 
                that was not a clever way to launch a writing career. She is perhaps one of the few students who liked 
                hearing that an assignment would require research and a "term paper" or "theme" of several pages.</p>
                <p class="p1-about">In high school, her English teacher asked students to write a paper on how to do 
                something. That night she wrote her essay. The next morning in study hall, she was baffled by inquiries 
                about what she'd written about. Slowly, it dawned on her that Mr. Michael's assignment was "How to Tie a 
                Shoe" or "How to Harvest Corn" or any of a thousand other "how to" tasks. Margery had written on how to 
                do anything, not how to do some thing. She still got an A, but not without a little anxiety and 
                apprehension.</p>
								<p class="p1-about">Margery completed her degrees in Psychology and English, added a year in seminary, 
                and began working for television in Chicago. Then she met her husband and soon they were involved in 
                Paul's church ministry and raising their children. Margery believes marriage and children are two sure 
                methods to make a person less selfish; her passion for writing was on a back burner while she devoted her 
                energies to being a wife and mother, and to several part-time jobs that fit with their ministry location 
                and the family's schedule and needs. Occasionally she was able to give attention to the ideas she was 
                jotting down on scraps of paper, knowing her writing would have to wait until her more important 
                responsibilities were accomplished.</p>
								<p class="p1-about">When Margery turned sixty, her children were in college or settling into marriages and 
                careers of their own. She quit her best paying part-time job and began pursuing writing opportunities. 
                Her articles in newspapers led to her having a weekly column in two newspapers and other writing 
                assignments. Those were satisfying, but she had a novel or two begging for attention. Gradually she 
                prioritized her schedule so that she could research both Victorian England and the Civil War, as well as 
                family history and letters kept by her Great-grandmother during the 1860s. Those facts were sifted into 
                the novel entitled "Leaves That Did Not Wither". The antagonist in that novel, though, is begging to tell 
                her side of the story, and the runaway son wants his pages, too, so those novels are being fine-tuned 
                before making their appearance.</p>
								<p class="p1-about">Margery took the challenge of assembling some of her writings from times she couldn't 
                resist holding a pen or clicking keys on the computer during her husband's ministry, and added new 
                thoughts to create "Christmas Musings" and the eBooks that make up that book.</p>
								<p class="p1-about">Recently her husband asked her if she was glad she had time to write now, and she 
                so appreciates his willingness to help insure that her schedule permits time for her writing passion. 
                She says she has many characters who think she owes them life, and she'll do what she can to let them 
                have their say in one form or another. Some characters may only be promised a succinct thought, others 
                a few lines in a poem, but those that tend to make her forget about making a meal or getting to bed will 
                likely land on the pages of short stories and novels, unless they extract too much of her energy 
                beforehand.</p>
