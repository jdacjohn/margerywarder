								<p class="p1-about"><a href="<?php print($siteRoot); ?>" title="Margery Kisby Warder - Writer, Speaker">Margery Kisby Warder</a> earlier
                served the Lord as a Speaker, Freelance Writer, Columnist, and Bible Teacher-Core Leader, before concentrating more
                efforts as a Christian Fiction Author. Since her husband's retirement and their move to Oklahoma, she spends more time
                compiling stories for future publications, participating in established Bible studies and para-church activities, and 
                discovering the joys of being a grandmother. She remains interested in speaking opportunities and has maintained her
                sense of humor, so feel free to contact Margery if you would like the author to speak at your upcoming event.</p>
                <p class="p1-about">While Ezra 7:10 guides her spiritual life, instructing her to study God's Word, apply 
                it in her own life, and then teach it to others, Margery Kisby Warder brings Biblical insights and humor 
                to her writing. Her writing motto is "<a href='http://eveninfiction.blogspot.com'>Truth & Humor, Even in Fiction</a>." As a Columnist, Margery offered a 
                variety of opinions including insights from the Bible, family life, humor, political concerns, and other 
                topics that she, as a pastor's wife, thought entertaining or necessary. Margery and her husband, Paul, 
                have two married children involved in ministry and they are happy to tell you about their three 
                granddaughters.</p>
